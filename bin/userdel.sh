#!/bin/bash

# 使い方
# userdel.sh user
#####################################################################

# 引数の解析
CMDNAME=`basename $0`

if [ $# -ne 1 ]; then
    echo "Usage: $CMDNAME user" 1>&2
    exit 1
fi

#####################################################################
# ユーザ削除
#####################################################################
userdel -r $1

#####################################################################
# htpasswd削除
#####################################################################
htpasswd -D /var/www/.htpasswd $1


exit 0
